package typecasting;
public class Bytetoshort {
    public static void main(String[] args) {
        // Define age as a byte
        byte ageInByte = 25; // Replace 25 with your actual age if different

        // Convert byte to short
        short ageInShort = ageInByte;

        // Print the results
        System.out.println("Age in byte: " + ageInByte);
        System.out.println("Age in short: " + ageInShort);
    }
}
