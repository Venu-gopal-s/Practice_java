package typecasting;
public class Int_double {
	public static void main(String[] args) {
//converting int into double 
		int a =100;
      double b1 = a;
      System.out.println(b1);
  //converting  double into int
      double a1= 80.80;
      int i2 = (int) a1;
      System.out.println(i2);
	}

}
